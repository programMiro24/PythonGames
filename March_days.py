day = int(input("Въведи дата от месец Март: "))
if 1<=day<=31:
    if day == 1:
        print("Честита Баба Марта!")
    elif day == 3:
        print("Честито Освобождение!")
    elif day == 8:
        print("Честит 8 Март - Денят на жената!")
    elif day == 21:
        print("Честита Първа Пролет!")
    else:
        print("Днес не е празник.")
else:
    print("Не си въвел дата от месец Март!")
